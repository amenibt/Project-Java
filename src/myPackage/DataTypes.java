package myPackage;

public class DataTypes {

	public static void main(String[] args) {
		
		// Premitive Data Types
		int a=10;
		double d=10.5;
		char c='H';
		boolean b= false;
		
		// Drived Data Types
		String s="welcome";
				
		System.out.println(a);
		System.out.println(d);
		System.out.println(c);
		System.out.println(b);
		System.out.println(s);
		
				

	}

}
